def find_region_code(keyword: str) -> tuple[str, str] | None:
    """키워드(지역명, 역이름 등)로 법정동 코드를 찾는다.

    Returns:
        (구이름, 코드) 또는 None
    """
    # 장소 데이터에서 검색
    if keyword in LOCATION_DATA:
        region = LOCATION_DATA[keyword][0]
        return region, REGION_CODES[region]

    # "역" 붙여서 검색
    station_key = keyword if keyword.endswith("역") else keyword + "역"
    if station_key in LOCATION_DATA:
        region = LOCATION_DATA[station_key][0]
        return region, REGION_CODES[region]

    # 직접 지역코드 검색
    if keyword in REGION_CODES:
        return keyword, REGION_CODES[keyword]

    # 부분 매칭
    for name in LOCATION_DATA:
        if keyword in name or name in keyword:
            region = LOCATION_DATA[name][0]
            return region, REGION_CODES[region]

    for name, code in REGION_CODES.items():
        if keyword in name or name in keyword:
            return name, code

    return None


def find_location(keyword: str) -> tuple[str, str, float, float] | None:
    """키워드로 장소의 (이름, 소재구, 경도, 위도)를 찾는다."""
    # 정확 매칭
    if keyword in LOCATION_DATA:
        region, x, y = LOCATION_DATA[keyword]
        return keyword, region, x, y

    # "역" 붙여서 검색
    station_key = keyword if keyword.endswith("역") else keyword + "역"
    if station_key in LOCATION_DATA:
        region, x, y = LOCATION_DATA[station_key]
        return station_key, region, x, y

    # 부분 매칭
    for name, (region, x, y) in LOCATION_DATA.items():
        if keyword in name or name in keyword:
            return name, region, x, y

    return None
